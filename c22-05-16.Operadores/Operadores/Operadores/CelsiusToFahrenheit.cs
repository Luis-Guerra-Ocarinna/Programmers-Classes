namespace Exercise4
{
    public class CelsiusToFahrenheit
    {
        /*
          Desenvolva um algoritmo para converter a temperatura em graus Celsius para
          Fahrenheit, a fórmula é F = (9 * C + 160) / 5	
        */
        public static double Run(double celsius)
        {
            return (9 * celsius + 160) / 5;
        }
    }
}
